# **************************  Introduction to OOPs in Python  **************************

# Procedural Programming: It is a function based programming generally preffered in action oriented tasks. If we have a scenario where we have to perform a set of actions then we can use procedural programming.

# Object Oriented Programming: It is a data based programming generally preffered in state oriented tasks. If we have a scenario where we have to maintain a state then we can use object oriented programming.
