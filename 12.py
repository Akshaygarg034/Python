# ***************************  Strings Slicing and Operations on Strings  ***************************

str = "Akshay"

print(1, str[1:4])
print(2, str[1:])
print(3, str[:4])
print(4, str[:])
print(5, str[1:6:2]) #str[1st index, last index, step size]
print(6, str[-4:-1]) # same as str[6-4: 6-1]
print(7, str[::-1]) # reverse the string
